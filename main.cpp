#include <iostream>
using namespace std;
int main() {
    int digit;
    cout << "Enter a digit: ";
    cin >> digit;

    switch (digit) {
        case 0:
            cout << "zeRO";
            break;
        case 1:
            cout << "one";
            break;
        case 2:
            cout << "TWO";
            break;
        case 3:
            cout << "THREE";
            break;
        case 4:
            cout << "fOUR";
            break;
        case 5:
            cout << "fIVE";
            break;
        case 6:
            cout << "SIX";
            break;
        case 7:
            cout << "SEVEN";
            break;
        case 8:
            cout << "EIGHT";
            break;
        case 9:
            cout << "NINE";
            break;
        default:
            cout << "ERRORRRRRRR";
            break;
    }

    return 0;
}
